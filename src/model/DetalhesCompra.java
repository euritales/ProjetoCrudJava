package model;

public class DetalhesCompra {
		
	private Double precoEstendido;
	private int qtdCompra;
	private int numeroSequencia;
	private int idProduto;
	private int codigoCompra;
	
	public Double getPrecoEstendido() {
		return precoEstendido;
	}
	public void setPrecoEstendido(Double precoEstendido) {
		this.precoEstendido = precoEstendido;
	}
	public int getQtdCompra() {
		return qtdCompra;
	}
	public void setQtdCompra(int qtdCompra) {
		this.qtdCompra = qtdCompra;
	}
	public int getNumeroSequencia() {
		return numeroSequencia;
	}
	public void setNumeroSequencia(int numeroSequencia) {
		this.numeroSequencia = numeroSequencia;
	}
	public int getIdProduto() {
		return idProduto;
	}
	public void setIdProduto(int idProduto) {
		this.idProduto = idProduto;
	}
	public int getCodigoCompra() {
		return codigoCompra;
	}
	public void setCodigoCompra(int codigoCompra) {
		this.codigoCompra = codigoCompra;
	}
	
	
}
